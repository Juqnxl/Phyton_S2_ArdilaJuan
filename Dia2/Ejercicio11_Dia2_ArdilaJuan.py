# Algoritmo para obtener el area de un circulo

# Ingrese el radio del circulo
Radio = float(input("Ingrese el radio del circulo: "))

# Calcula el area del circulo
Pi = 3.1416
Area = Pi * Radio ** 2

# Muestra el resultado
print("El area del circulo es: ", Area)

