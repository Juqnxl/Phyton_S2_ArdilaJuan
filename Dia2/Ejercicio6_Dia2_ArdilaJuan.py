# Algoritmo para saber si es par o impar el numero

# Ingrese un numero
numero = int(input("Ingrese un numero entero: "))

# Verificacion para verificar si un numero es par
if numero % 2 == 0:
    print("El numero es par")
else:
    print("El numero es impar")

